// Item probability configuration for a single position in a room size
struct PositionItemProbabilities {
    u16 probabilities[19]; // Probability for each item (0-18) for this position
};

// Item probability configuration for a single room size (all positions)
struct RoomSizeItemProbabilities {
    PositionItemProbabilities positions[12]; // Probabilities for each position (0-11)
};

// Complete item distribution configuration for all room sizes
struct ItemDistributionConfig {
    RoomSizeItemProbabilities roomSizes[11]; // Indexed by RoomSizeCategory values (2-12)
};

